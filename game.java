public abstract class game {
   
    public void play(){
        System.out.println("Playing...");
    }


    public void initialize() {
        System.out.println("MathGame Initialized! Start playing.");
    }

    public void startPlay(){
        System.out.println("MathGame Started. Enjoy the game!");
    }

  
    public void endPlay(){
        System.out.println("MathGame Finished!");
    }
}
